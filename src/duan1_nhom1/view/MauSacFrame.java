    public void loadTable() {
        List<MauSac> listMauSac = mauSacService.getAll();
        DefaultTableModel dfm = (DefaultTableModel) tbl_mausac.getModel();

        dfm.setRowCount(0);

        for (MauSac mauSac : listMauSac) {
            Object[] row = new Object[]{
                mauSac.getMa(),
                mauSac.getTen(),
                mauSac.getMoTa(),
                mauSac.getNgaySua(),
                mauSac.getNgayTao()
            };
            dfm.addRow(row);

        }

    }
